/*  creating the database called university     */
create database university;

/* open the database  */
use university;


/* creating the department table
   the auto_increment column (deptID) allows the ID to be feed automatically.
   its value start with 1, and increment by one.
   when a table contain auto_increment column
   that column should be a primary key (part or primary key)    */
create table department
(
deptID int auto_increment,
deptCode varchar(50) not null unique,
deptName text not null,
primary key(deptID)
)engine=innoDB;


/*creating the program table   */
create table program
(
progID varchar(100) not null unique,
progName text not null,
capacity int(4) not null  default 600,
ntaLevel int(2)  not null,
department varchar(50),
primary key(progID),
foreign key(department) references department(deptCode) on update cascade on delete set null
)engine=innoDB;



/* creating the student table    */
create table student
(
studentID varchar(100) not null unique,
fullname text not null,
gender enum('male', 'female') not null,
dob date not null,
nationality varchar(100) not null default 'Tanzanian',
maritualStatus varchar(100) not null default 'single',
program varchar(100),
YoE int(4) not null,
active enum('yes', 'no') not null default 'yes',
status varchar(100) not null default 'continuing',
primary key(studentID),
foreign key(program) references program(progID) on update cascade on delete set null
)engine=innoDB;


/*  creating the courses table   */
create table courses
(
courseID int auto_increment,
courseCode varchar(20) not null unique,
courseName text not null,
credit int(2) not null default 9,
elective enum('yes', 'no') not null default 'no',
primary key(courseID)
)engine=innoDB;


/*  creating the enrollment table   */
create table enrollment
(
studentID varchar(100) not null, 
courseCode varchar(20)  not null,
semester int(1) not null,
aYear varchar(15) not null,
primary key(studentID, courseCode),
foreign key(studentID) references  student(studentID) on update cascade on delete cascade,
foreign key(courseCode) references  courses(courseCode) on update cascade on delete cascade
)engine=innoDB;


